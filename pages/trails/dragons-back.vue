      <p>
        Dragon’s Back, or North Mountain, looms over the mountain bike landscape literally and figuratively, a monster
        of a thing. The daunting profile clearly explains the nickname, becoming only more specific when riding across
        the 12 mile spine--heavily landscaped with natural slate armor plates and limestone scales that clank and creak
        at times beneath the wheels. When the leaves are down, a perpetual spectacular view to the east and west are
        presented along the sometimes inches wide path, as the thunder lizard stretches out with knobs and peaks and a
        gentle curve towards the north.
      </p>
      <blog-image :src="img + 'P1020274.jpg'"/>
      <p>
        Bicycle exploration began in earnest in early 1990, first by national forest printed map, then by personal
        contact. It was decided that since the ridge was only 12 or so miles long, we’d do it end to end, float tripping
        as it were with automobiles stashed at either end. In summary, a projected 3 hour tour became an all day ordeal,
        with cranks falling off, fire anthills swarming, and invisible ridgeline trails. After a complete asswhupping
        taken, it was decided this place is definitely something special.
      </p>
      <p>
        Paul Economy and Scott Freday, The Lads of Virginia in those days, had fashioned in 1989 a backcountry race
        several ridges west on mighty Potts Mountain, called The Great Craig County Escape. Mountain bike racing was
        rare enough in those days, and that type of public land backcountry 26 mile epic remains so today. Having
        already explored North Mountain a bit more, I suggested to the Lads that a new race be added to the Craig County
        collection in 1992, and that it should be called Dragon’s Back. It remained a suggestion and never materialized
        into being. The Great Escape even went missing that next year. A lot of work, this race promotion business, and
        I needed help to make this reality. So Dragon’s Back stayed tucked away for a time.
      </p>
      <p>
        That time was late in 1992 when East Coasters bike shop opened up beside a buffet trough in Roanoke. The first
        manager, John Corliss, envisioned ambitious plans for the shop with a race being one of those. I presented the
        Dragon’s Back idea and the rest, as they say, is history. We took one giant step further--lashing it together
        with the well established Rowdy Dawg in Blacksburg along with the new Thunderr In Holy Land in Thaxton. The
        Virginia State Championship Mountain Bike Series was born, and the second ever mountain bike race series east of
        the rockies (West Virginia’s WVMBA series began a year earlier) was born.
      </p>
      <blog-image :src="img + 'dragons-back-flyer.jpg'"/>
      <p>
        April 4, 1993, Dragon’s Back debuts to what would be 15 year run as the lead out race of the series, inspiring
        more than one competitor to dub it, in NASCAR reference, the Daytona of Virginia. Mother Nature is to credit for
        that first position because Rowdy Dawg was scheduled to lead out two Sundays earlier. But as things go, a freak
        March 13 blizzard piled on 18” of drifting snow that hung around for weeks, and was then slotted the week
        following Dragon’s Back I.
      </p>
      <p>
        For two years the course climbed Deer Trail, rode the ridge to a Turkey descent, and then knitted together with
        the rolling gravel of Wildlife Road. Diamond Back pro Gunnar Shogren from Morgantown, WV took the debut win,
        intimating at the time that Dragon’s Back was clearly unlike any other. Mr. Shogren returned to defend in ‘94
        but was thwarted by the 19 year old Junior National Champion named Floyd Landis.
      </p>
      <p>
        In subsequent years, all three ridge access trails were incorporated in a figure eight pattern that would remain
        until the end: up Grouse, right to Deer descent, up Grouse, left to Turkey descent, and dirt road scramble back
        to the start/finish--only 18 miles in length.
      </p>
      <p>
        One memorable event in the late 90’s...some yayhoos fresh from shooting a turkey, then beer-bonging a case of
        PBR, discharged their 12 gauge towards a racer out for a Saturday pre-ride. Then they drove wildly and
        recklessly up and down the road in a menacing manner. A payphone miles away was sought while we kept an eye out
        for quick cover, and the feds were summoned. This lead to a lengthy investigation, with several camping
        racers-now-witnesses questioned and ultimately arrests. Two criminals were charged and convicted in federal
        court with racially motivated assault with a deadly weapon that lead to lengthy prison terms. This lead to my
        first federal subpoena and thankfully the only one to date.
      </p>
      <p>
        The first Dragon’s Back XXC (or marathon in USAC land) arrived in 2000 just in time to kick off the millenium.
        As the second ever XXC (Misty Mountain Hop the first), the 40 mile course used the entire North Mountain ridge
        trail, and was conceived around the notion: less a race, more a long day’s ride. Beginning in morning, the XXC
        racers would mingle with the regular 18 mile XC racers, blending to finish all at around the same time. XXC was
        a hit immediately and now comprises half the participants of entire events--XC plus XXC combined. We have the
        land, we must ride it! It’s astonishing that our quaint notion of ride quickly morphed into race with finish
        times sub 4 hours!
      </p>
      <p>
        The last of the original run of Dragon’s Backs was April 15, 2007, a dark, deeply gray and damp cold spring day.
        This is memorable because, while the race was well attended and successfully completed despite conditions,
        things would grow very dark one day later just a few miles away in Blacksburg when 31 people, and their
        murderer, would perish on the campus of Virginia Tech. Our thoughts remain with those affected to this and every
        day, and we wish no other similar instance would ever befall innocent people, yet they do.
      </p>
      <p>
        Aside from being a premier racing venue, Dragon’s Back remains well ridden, and always challenging to this day.
        The North Mountain Trail has even earned the blue color coding on the National Geographic map #788 notation as a
        mountain bike trail, the only such designation on that map or many others in the Virginia NatGeo collection.
      </p>
